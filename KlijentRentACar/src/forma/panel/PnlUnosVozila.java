    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
     
        try {
            String km = txtKilometraza.getText();
            String tipVozila = txtTipVozila.getText();
            String br = txtBrojPutnika.getText();
            String ce = txtCenaPoDanu.getText();
            String marka = txtMarka.getText();
            

            Zaposleni z = sesija.Sesija.getInstanca().getZaposleni();

            boolean val = validacija(km, tipVozila, br, ce, marka);

            if (val) {
                
                 double cena = Double.parseDouble(ce);
                 double kilometraza = Double.parseDouble(km);
                 int brojPutnika = Integer.parseInt(br);
                 
                Vozilo v = new Vozilo((long) 0, kilometraza, tipVozila, brojPutnika, cena, z, marka);
                try {
                    String poruka = Kontroler.getInstanca().unesiVozilo(v);
                    JOptionPane.showMessageDialog(this, poruka);
                    sesija.Sesija.getInstanca().getMapa().put("trenutnoVozilo", v);
                    ((FrmDialog) SwingUtilities.getWindowAncestor(this)).dispose();
                    if (((SwingUtilities.getWindowAncestor((FrmDialog) SwingUtilities.getWindowAncestor(this)))) instanceof GlavnaForma) {
                        return;
                    }
                    (((FrmForma) SwingUtilities.getWindowAncestor((FrmDialog) SwingUtilities.getWindowAncestor(this)))).getPanel().popuniPodatke();
                } catch (NullPointerException e) {
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Neuspesno cuvanje");
                    Logger.getLogger(PnlUnosKlijenta.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Morate uneti rboj");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void pripremiFormu(ModForme model) {
        switch (model) {
            case unos:
                btnSacuvaj.setVisible(true);

                break;
            case izmena:
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutnoVozilo"));
                btnSacuvaj.setVisible(false);

                break;
            case pregled:
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutnoVozilo"));
                btnSacuvaj.setVisible(false);

                txtKilometraza.setEditable(false);

                txtTipVozila.setEditable(false);
                txtBrojPutnika.setEditable(false);
                txtCenaPoDanu.setEditable(false);
                txtMarka.setEditable(false);

                break;
        }
    }

