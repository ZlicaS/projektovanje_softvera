    private boolean validacija(String km, String tipVozila, String br, String ce, String marka) {

        if (km.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;
        }
        if (tipVozila.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;
        }
        if (br.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;
        }
        if (ce.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;
        }
        if (marka.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;
        }
        
            

        return true;
    }

    @Override
    public void popuniPolja(OpstaDomenskaKlasa odk) {
        Vozilo v = (Vozilo) odk;
        txtMarka.setText(v.getMarka());
        txtKilometraza.setText(v.getKilometraza() + "");
        txtTipVozila.setText(v.getTipVozila());
        txtBrojPutnika.setText(v.getBrojPutnika() + "");
        txtCenaPoDanu.setText(v.getCenaPoDanu() + "");
    }

