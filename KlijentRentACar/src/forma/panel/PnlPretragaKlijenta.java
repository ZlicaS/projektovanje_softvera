    private void pripremiTabelu() {
        model = new ModelTabeleKlijent();
        tblKlijenti.setModel(model);
    }

    private void pripremiDialog(ModForme modForme) {
        if (modForme != ModForme.unos) {
            int index = tblKlijenti.getSelectedRow();

            if (index == -1) {

                JOptionPane.showMessageDialog(this, "Morate izabrati red!");
                return;
            }
            Klijent k = model.getLista().get(index);
            sesija.Sesija.getInstanca().getMapa().put("trenutniKlijent", k);
        }

        PnlUnosKlijenta panel = new PnlUnosKlijenta(modForme);
        FrmDialog dialog = new FrmDialog((Frame) SwingUtilities.getWindowAncestor(this), true, panel);
        dialog.setVisible(true);
    }

    void izaberi() {
         btnIzaberi.setVisible(true);
    }

    private void vratiSve() {
        try {
            model = new ModelTabeleKlijent();
            List<Klijent> lista = kontroler.Kontroler.getInstanca().vratiSveKlijente();

            for (Klijent k : lista) {
                model.ubaci(k);
            }
            tblKlijenti.setModel(model);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }
    }
