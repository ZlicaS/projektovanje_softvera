    private void btnVratiSveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVratiSveActionPerformed
        try {
            model = new ModelTabeleKlijent();
            List<Klijent> lista = kontroler.Kontroler.getInstanca().vratiSveKlijente();

            for (Klijent k : lista) {
                model.ubaci(k);
            }
            tblKlijenti.setModel(model);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }//GEN-LAST:event_btnVratiSveActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String imeIPrezime = txtImePrezime.getText();
        if (imeIPrezime.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli kriterijum izbora");
            return;
        }
        Klijent kl = new Klijent();
        kl.setImePrezimeKlijent(imeIPrezime);
        List<Klijent> lista;
        model = new ModelTabeleKlijent();
        try {
            lista = kontroler.Kontroler.getInstanca().vratiKlijente(kl);

            for (Klijent k : lista) {
                model.ubaci(k);
            }
            tblKlijenti.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            vratiSve();
            Logger.getLogger(PnlPretragaKlijenta.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_btnPretraziActionPerformed

