 private void srediTabelu() {
        TableModel model = new TableModel();
        tblZaposleni.setModel(model);
    }

    public void dodajPrijavljenogZaposlenog(Zaposleni z) {
        TableModel model = (TableModel) tblZaposleni.getModel();
        model.dodajZaposlenog(z);
    }

    public void odjaviZaposlenog(Zaposleni z) {
        TableModel model = (TableModel) tblZaposleni.getModel();
        model.izbaciZaposlenog(z);
    }

    public void popuniTabelu() {
        TableModel model = new TableModel();
        tblZaposleni.setModel(model);
    }

    public void postaviStatus(String poruka, boolean signal) {

        lblStatus.setText(poruka);
        btnPokreni.setEnabled(signal);
        btnZaustavi.setEnabled(!signal);
        pnlZaposleni.setVisible(!signal);
        pnlKonfiguracija.setVisible(signal);

    }

