    private void btnOdjaviZaposlenogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdjaviZaposlenogActionPerformed
       TableModel model = (TableModel) tblZaposleni.getModel();
        int index = tblZaposleni.getSelectedRow();
        if (index != -1) {
            Zaposleni z = model.izbaciRed(index);
            Kontroler.getInstanca().izbaciZaposlenog(z);
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati red tabele!");
        }
    }//GEN-LAST:event_btnOdjaviZaposlenogActionPerformed

